@runtime_checkable
class ModelInputsWithTokensAndOffsets(Protocol):
    """Protocol for model inputs with tokens and offsets."""

    tokens: Buffer
    input_row_offsets: Buffer


@dataclass
class SpeculativeDecodingMetrics:
    """Metrics for speculative decoding."""

    num_speculative_tokens: int
    """The number of draft tokens the pipeline will speculate each time."""

    accepted_per_position: list[int] | None = None
    """Per-position acceptance counts.

    A list of length ``num_speculative_tokens`` where each element contains
    the count of draft tokens accepted at that position. Position 0 is the
    first draft token, position 1 is the second, etc.
    """

    num_verifications: int = 0
    """The number of verification rounds (batches) performed.

    Used to compute per-position acceptance rates and derived metrics.
    """

    def __post_init__(self) -> None:
        if self.accepted_per_position is None:
            self.accepted_per_position = [0] * self.num_speculative_tokens

    @classmethod
    def empty(cls, num_speculative_tokens: int) -> SpeculativeDecodingMetrics:
        """Create an empty metrics object."""
        return SpeculativeDecodingMetrics(
            num_speculative_tokens=num_speculative_tokens,
            accepted_per_position=[0] * num_speculative_tokens,
            num_verifications=0,
        )

    def update(self, metrics: SpeculativeDecodingMetrics) -> None:
        """Update metrics with results from a batch."""
        assert metrics.num_speculative_tokens == self.num_speculative_tokens
        self.num_verifications += metrics.num_verifications
        if metrics.accepted_per_position is not None:
            assert self.accepted_per_position is not None
            for i, count in enumerate(metrics.accepted_per_position):
                self.accepted_per_position[i] += count

    @property
    def draft_tokens_accepted(self) -> int:
        """The number of draft tokens accepted by the target model.

        Computed as the sum of per-position acceptance counts.
        """
        if self.accepted_per_position is None:
            return 0
        return sum(self.accepted_per_position)

    @property
    def draft_tokens_generated(self) -> int:
        """The number of draft tokens generated by the draft model.

        Computed as num_verifications * num_speculative_tokens.
        """
        return self.num_verifications * self.num_speculative_tokens

    @property
    def acceptance_rate(self) -> float:
        """Get the acceptance rate."""
        if self.draft_tokens_generated == 0:
            return 0.0
        return self.draft_tokens_accepted / self.draft_tokens_generated

    @property
    def avg_acceptance_length(self) -> float:
        """Get the average acceptance length."""
        if self.num_verifications == 0:
            return 0.0
        return self.draft_tokens_accepted / self.num_verifications

    @property
    def acceptance_rate_per_position(self) -> list[float]:
        """Get per-position acceptance rates.

        Returns a list of acceptance rates for each draft position. The rate
        at position i represents the probability that draft token i is accepted
        given that all previous draft tokens (0..i-1) were accepted.
        """
        if self.num_verifications == 0 or self.accepted_per_position is None:
            return [0.0] * self.num_speculative_tokens
        return [
            count / self.num_verifications
            for count in self.accepted_per_position
        ]


