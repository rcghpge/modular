from __future__ import annotations

from max.graph import TensorValue, ops


def rotate_half(x: TensorValue) -> TensorValue:
    """Rotates half the hidden dims of the input."""
    x1 = x[..., : x.shape[-1] // 2]
    x2 = x[..., x.shape[-1] // 2 :]
    return ops.concat((-x2, x1), axis=-1)
